enc = open('TsaiBroSaid').read()
enc = enc.split('\n')[1]
enc = enc[2:].split('發財')
enc = [c.count('.') for c in enc]

table = [
    0x35, 0x36, 0x37, 0x38, 0x39, 0x7B, 0x7D, 0x5F,
    0x57, 0x58, 0x59, 0x30, 0x79, 0x7A, 0x41, 0x42,
    0x61, 0x62, 0x63, 0x64, 0x6D, 0x6E, 0x6F, 0x70,
    0x53, 0x54, 0x55, 0x56, 0x47, 0x48, 0x49, 0x4A,
    0x4B, 0x4C, 0x4D, 0x4E, 0x75, 0x76, 0x77, 0x78,
    0x65, 0x66, 0x67, 0x68, 0x71, 0x72, 0x73, 0x74,
    0x69, 0x6A, 0x6B, 0x6C, 0x4F, 0x50, 0x51, 0x52,
    0x43, 0x44, 0x45, 0x46, 0x31, 0x32, 0x33, 0x34]

flag = b''
for i in range(0, len(enc), 2):
    code = enc[i:i+2]
    flag += bytes([table[(code[0] - 1) * 8 + (code[1] - 1)]])
print(flag)
